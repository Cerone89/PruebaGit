var color = prompt ("Ingrese un color");

switch(color) {

    case "rojo":
    console.log("rojo");
    break;

    case "azul":
    console.log("azul");
    break;

    case "amarillo":
    console.log("amarillo");
    break;

    default:
        console.log("El color ingresado no es valido");
        break;




}